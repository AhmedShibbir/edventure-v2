    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $ImageUpload = ImageUpload::find($id);
        $delete = $ImageUpload->delete();
        if ($delete) {
            return redirect()->route('upload-image.index')->with('status', 'Image successfully deleted!');
        } else {
            return redirect()->route('upload-image.index')->with('failed', 'Image deletion failed!');
        }
